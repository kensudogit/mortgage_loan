#!/bin/bash

echo "========================================"
echo "住宅ローンシステム テスト実行スクリプト"
echo "========================================"

echo ""
echo "1. バックエンドテストを実行中..."
mvn test

if [ $? -ne 0 ]; then
    echo "バックエンドテストが失敗しました。"
    exit 1
fi

echo ""
echo "2. フロントエンドテストを実行中..."
cd frontend
npm test -- --watchAll=false

if [ $? -ne 0 ]; then
    echo "フロントエンドテストが失敗しました。"
    exit 1
fi

echo ""
echo "========================================"
echo "すべてのテストが正常に完了しました！"
echo "========================================" 